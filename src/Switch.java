public class Switch {
    public static void main(String[] args) {

        String danie = "Jedzenie";

        switch (danie)  {
            case "pizza":
                System.out.println("Pizza cena ");
                break;
            case "Jedzenie" :
                System.out.println("Cena jedzenia ");
                break;
            default:
                System.out.println("Brak");

        }




    }
}
