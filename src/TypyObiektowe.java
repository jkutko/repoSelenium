public class TypyObiektowe {

    public static void main(String[] args) {

      /*
      *  System.out.println("Hello!!!");
      * */


        // liczby staloprzecinkowe
        Byte firstNumber = 123;
        Short secondNumber = 23454;
        Integer thirdNumber = 3343432;
        Long fourthNumber = 2134567L;

        // liczby zmiennoprzecinkowe
        Float fifthNumber = 4.9993F;
        Double sixnumber = 3.999999999D;

        // wartość logiczna
        Boolean prawda = true;
        Boolean falsz = false;

        // pojedynczy znak
        Character letter = 'A';

        // ciag znaków
        String hello = "Hejka";

        System.out.println(hello.charAt(0));
        System.out.println(hello.contains("Hej"));

    }
}
